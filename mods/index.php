        <h1>Mods</h1>
        <p>Por primera vez el servidor de <b>Minecraft UN</b> utilizará mods en su host, con el fin de brindar una experiencia  de  juego  dinámica y  amena  para  los  usuarios. Al final de este articulo usted podrá seguir una guia de como instalarlos. Los mods utilizados son los siguientes:</p>

        <h3>Security Craft</h3>
        <p> Añade exactamente lo que el nombre sugiere: láser, escáneres de retina, teclados, puertas irrompibles y más. Se adapta bien a mods como Smart Moving, Secret Rooms y el mod de Wall Jump. Ahora tiene la opción de configurar contraseñas y códigos clave  para  acceder  a  través  de  puertas  que  son  inexpugnables,  excepto  por  el propietario.</p>
        <img src="SecurityCraft.png" alt="Security Craft">

        <h3>Alex  mobs</h3>
        <p>Es un mod que se encargará de añadir hasta quince nuevos tipos de criaturas que se adaptan muy bien al diseño original de Minecraft. La gran mayoría representan animales reales, mientras que una pequeña parte representan a criaturas  fantásticas. En cuanto a animales podremos encontrar osos pardos, moscas, langostas, cocodrilos, gorilas, monos capuchinos, dragones de Komodo, orcas, tiburones martillo, serpientes de cascabel, correcaminos, colibrí y ciempiés. En cuanto a criaturas fantásticas encontraremos a Sunbird, Bone Serpent, Crimson Mosquito y el Endergrade.</p>
        <img src="AlexMobs.png" alt="Alex Mobs">

        <h3>MrCrayfish's Furniture Mod</h3>
        <p>Es un mod que nos permitirá fabricar una enorme cantidad de mobiliario, decorativo y funcional, para nuestras construcciones en Minecraft. Este mobiliario está pensado para diversas partes de las viviendas, es decir, podremos crear mobiliario de cocina, de baño, de salón y comedor, mobiliario para las habitaciones, y decoración para jardines y exteriores. Podremos fabricar y utilizar hornos, refrigeradores, congeladores, lámparas, mesas, sofás, sillas, retretes, duchas, bañeras, cortinas, estanterías, armarios de cocina, fuegos de suelo, taburetes, butacas, campanas extractoras, luces, papeleras, fuentes de agua y surtidores, ordenadores, radios, cuadros, platos, vasos, tostadoras, y mucho más.</p>
        <img src="MrCrayfishFurnitureMod.png" alt="MrCrayfish Furniture Mod">

        <h3>Chisel</h3>
        <p>Un mod que nos ofrece una gran cantidad y variedad de bloques de construcción y decoración, que podremos utilizar para crear construcciones con nuevos bloques decorativos, de nuevos colores, con nuevas texturas y con diseños variados. Todo ello se podrá gestionar desde un sólo objeto, un cincel. Este cincel nos permitirá acceder a una interfaz donde podremos crear todas las variantes de bloques decorativos. Es decir, si colocamos el cincel en la mano del personaje y pulsamos botón derecho del ratón, se abrirá la interfaz. Una vez aquí, podremos colocar un bloque cualquiera del juego y justo a la derecha del mismo aparecerán las nuevas variantes de color, textura y diseño de ese bloque en particular.</p>
        <img src="Chisel.png" alt="Chisel">

        <h3>When  Dungeons  Arise</h3>
        <p>Es un mod que cuenta con varios patrones de mazmorras prediseñados, con estructuras bastante diferenciadas de las mazmorras predeterminadas del juego, que serán generados de forma aleatoria alrededor del mundo. Donde se generarán enemigos incluso durante el día. Las mazmorras que genera este mod aparecerán en varios formatos. Por ejemplo, encontraremos coliseos, palacios, templos con muchas salas y pasadizos, aviones y dirigibles, molinos de viento, casas, torres de magos, campamentos, barcos en los mares, minas ocultas y casas en los árboles, entre otras.</p>
        <img src="WhenDungeonsArise.png" alt="When Dungeons Arise">

        <h3>Emotecraft</h3>
        <p>Es un mod que nos permite realizar nuevos movimientos con nuestro personaje de Minecraft. Una gran parte de estas animaciones están basadas en las animaciones o bailes de Fortnite.</p>
        <img src="Emotecraft.png" alt="Emotecraft">

        <h3>VoxelMap</h3>
        <p>Es un mod que nos permitirá disponer de un minimapa personalizable en la pantalla de juego y nos mostrará el entorno que nos rodea de forma fiel al paquete de texturas que estemos utilizando en cada momento. Tanto el minimapa como el mapa a pantalla completa nos permitirá ver el mundo explorado en la dimensión del Overworld, de la dimensión del Nether y del End. Podremos ver el mapa en pantalla completa pulsando la tecla <i>«X»</i>. Ampliar y disminuir el área de terreno que aparecer en el minimapa utilizando la tecla <i>«Z»</i>. Marcar puntos de interés presionando la tecla <i>«N»</i>. Y acceder al menú de opciones de personalización pulsando la tecla <i>«M»</i>. Además, el mapa nos permitirá teletransportarnos a cualquier punto del mundo que previamente hayamos establecido.</p>
        <img src="VoxelMap.jpg" alt="Voxel Map">

        <h3>Modern Life</h3>
        <p> Es un mod que se centrará en ofrecernos contenido que, tal y como su propio nombre indica, se basa en elementos presentes en la vida moderna. Estos elementos incluyen electrodomésticos, mobiliario y tecnología, entre otras cosas interesantes. En estos momentos el mod nos permitirá hacer uso de elementos como cámaras de vídeo, motosierras, carreteras pavimentadas, velocímetros, puertas de cristal y linternas. También bicicletas, espejos, guitarras, lienzos y caballetes de pintura, tableros de ajedrez, cubos de basura, tocadiscos, microondas, refrigeradores, camas dobles, inodoros, estufas, radiadores, transmisores y receptores de redstone inalámbrica, entre otras cosas.</p>
        <img src="ModernLife.png" alt="Modern Life">

