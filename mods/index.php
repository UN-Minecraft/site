        <h3>Treasure2</h3>
        <p>Es un mod que se encarga de generar diferentes tesoros alrededor del mundo de Minecraft. Su objetivo es fomentar la exploración del mundo para conseguir tesoros escondidos. Cada tesoro requiere de una llave concreta para ser abierto, por lo que no será tan sencillo como simplemente abrir un cofre oculto, sino que también tendremos que encontrar la llave correspondiente a cada tipo de tesoro.</p>
